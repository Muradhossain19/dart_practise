main() {
  // Premative Data Type
  // Boolean => true/false -> bool
  // Positive/Nagative define kore
  // bool e true or false chara onno kichu rakha jabe nah

  bool isStudent = false;
  bool isEmployee = true;

  print(isStudent);
  print(isEmployee);

  bool isEven(int number) {
    return number % 2 == 0;
  }

  print(isEven(6)); // true
  print(isEven(5)); // false


}





